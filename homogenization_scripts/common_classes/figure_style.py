
class FigureStyle:
    def __init__(self, 
                 linewidth          =2.0, 
                 markersize         =6.0,
                 markeredgewidth    =2,
                 fontsize           =12,
                 paperformat        = False,
                 ):
        
        self.lw = linewidth
        self.ms = markersize
        self.mew = markeredgewidth
        self.fs = fontsize
        self.pf = paperformat
